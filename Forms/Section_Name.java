    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if ("".equals(sec.getText())) {
            JOptionPane.showMessageDialog(this, "املئ البيانات", "خطأ", JOptionPane.WARNING_MESSAGE);
        } else {
            String save = "INSERT INTO SECTIONS VALUES ('" + sec.getText() + "')";
            int co = JOptionPane.showConfirmDialog(this, "هل تريد الحفظ", "تأكيد", JOptionPane.WARNING_MESSAGE);
            if (co == JOptionPane.YES_OPTION) {
                boolean ifS = DB.GoDB.runNunQuery(save);
                if (ifS) {
                    JOptionPane.showMessageDialog(this, "تم حفظ البيانات", "نجح", JOptionPane.WARNING_MESSAGE);
                    DB.GoDB.fillCompo("sections", "Section", jsection);
                    jsection.setSelectedItem(sec.getText());
                    sec.setText("");
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "لم يتم الحفظ", "خطأ", JOptionPane.ERROR_MESSAGE);
                }

            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

